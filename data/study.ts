export function getUniversityBySlug(slug: string) {
  return universities.find((item) => item.slug === slug);
}

export function getCollegeBySlug(universitySlug: string, collegeSlug: string) {
  return getUniversityBySlug(universitySlug)?.colleges.find((item) => item.slug === collegeSlug);
}

export function getMajorBySlug(universitySlug: string, collegeSlug: string, majorSlug: string) {
  return getCollegeBySlug(universitySlug, collegeSlug)?.majors.find((item) => item.slug === majorSlug);
}
